// DealerOS — AI Orchestration Engine: Plan Bridge Runner (Sprint 11L Phase A)
//
// Orchestrates the complete live bridge run for one AI execution plan.
//
// Run order per plan:
//   1. Build runtime context from live request (reuses plan-runner.ts builder)
//   2. Build parallel step groups (reuses step-runner.ts)
//   3. For each step in topological order:
//      a. Run dry-run step validation (deps, feature gate, approval gate check)
//      b. If blocked: record failure, continue to count blocked steps
//      c. If skipped (optional): record as skipped
//      d. If validated or blocked_approval: run live bridge lifecycle preparation
//      e. If approval gate triggered: build pause record, stop chain
//      f. Write output key placeholders for downstream steps
//   4. Compute overall plan state
//   5. Return AIOrchestratorLiveBridgeResult
//
// Security rules:
//   - dealer_id always from getCurrentDealer() — injected in AILiveRuntimeRequest
//   - agent_context always pre-built by the calling server action
//   - No DB calls inside this module
//   - No external API calls
//
// Async — calls runStepBridgeLifecycle() which calls agent.initialize().

