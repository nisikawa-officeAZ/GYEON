// ─── Compliance validation ────────────────────────────────────────────────────

export interface DraftComplianceResult {
  passed:           boolean;
  flags:            ReviewComplianceFlag[];
  blocking_flags:   ReviewComplianceFlag[];
  warning_flags:    ReviewComplianceFlag[];
  checked_at:       string;
}

/**
 * Validates a draft message text against REVIEW_COMPLIANCE_RULES.
 *
 * Checks for prohibited patterns using substring matching.
 * This is a fast static pre-check — not a substitute for dealer review.
 *
 * Returns passed = true only when no blocking flags are detected.
 */
export function validateDraftCompliance(
  messageText: string,
  now:         string,
): DraftComplianceResult {
  const foundFlags: ReviewComplianceFlag[] = [];
  const lowerText  = messageText.toLowerCase();

  for (const rule of REVIEW_COMPLIANCE_RULES) {
    const jpMatch = rule.jp_patterns.some((p) => messageText.includes(p));
    const enMatch = rule.en_patterns.some((p) => lowerText.includes(p.toLowerCase()));
    if (jpMatch || enMatch) {
      foundFlags.push(rule.id);
    }
  }

  const blockingFlags = foundFlags.filter((f) => {
    const rule = REVIEW_COMPLIANCE_RULES.find((r) => r.id === f);
    return rule?.severity === "blocking";
  });

  const warningFlags = foundFlags.filter((f) => {
    const rule = REVIEW_COMPLIANCE_RULES.find((r) => r.id === f);
    return rule?.severity === "warning";
  });

  return {
    passed:         blockingFlags.length === 0,
    flags:          foundFlags,
    blocking_flags: blockingFlags,
    warning_flags:  warningFlags,
    checked_at:     now,
  };
}

// ─── Required message elements ────────────────────────────────────────────────

/**
 * ReviewComplianceRequirement — elements that should be present in a review request.
 * These are advisory checks — missing elements trigger warnings, not blocking failures.
 */
export interface ReviewComplianceRequirement {
  element:     string;
  description: string;
  /** Patterns that indicate this element is present. */
  jp_patterns: string[];
  en_patterns: string[];
}

export const REQUIRED_MESSAGE_ELEMENTS: ReadonlyArray<ReviewComplianceRequirement> = [
  {
    element:     "greeting_and_thanks",
    description: "Message should open with a greeting and thank the customer for their business.",
    jp_patterns: ["ありがとう", "感謝", "いつもお世話", "この度は"],
    en_patterns: ["thank you", "thanks for", "appreciate your"],
  },
  {
    element:     "voluntary_freedom",
    description: "Message must explicitly state the customer is free to write their own honest opinion.",
    jp_patterns: ["率直", "ご自由に", "お気持ち", "感じたこと", "自由に"],
    en_patterns: ["honest opinion", "freely", "whatever you feel", "your own words"],
  },
  {
    element:     "explicit_optionality",
    description: "Message must make clear the review is optional.",
    jp_patterns: ["もしよろしければ", "よろしければ", "ご都合がよければ", "お時間があれば"],
    en_patterns: ["if you'd like", "if you have time", "no pressure", "optional"],
  },
] as const;

/**
 * Checks a draft message for the presence of required elements.
 * Returns which required elements are present and which are missing.
 */
export function checkRequiredElements(
  messageText: string,
): { element: string; present: boolean }[] {
  return REQUIRED_MESSAGE_ELEMENTS.map((req) => {
    const jpPresent = req.jp_patterns.some((p) => messageText.includes(p));
    const enPresent = req.en_patterns.some((p) =>
      messageText.toLowerCase().includes(p.toLowerCase()),
    );
    return { element: req.element, present: jpPresent || enPresent };
  });
}

// ─── Draft construction ───────────────────────────────────────────────────────

/**
 * Builds an empty ReviewDraft for dealer manual composition.
 * Use this when the dealer writes the message themselves.
 *
 * The draft starts with compliance_passed = false — validateDraftCompliance()
 * must be called after the message is written.
 */
export function buildEmptyReviewDraft(
  id:         string,
  requestId:  string,
  dealerId:   string,
  platform:   ReviewPlatform,
  reviewUrl:  string | null,
  now:        string,
): ReviewDraft {
  return {
    id,
    request_id:       requestId,
    dealer_id:        dealerId,
    draft_type:       "review_request",
    message_text:     "",
    character_count:  0,
    platform,
    review_url:       reviewUrl,
    prepared_by:      "dealer",
    ai_provider_id:   null,
    approval_status:  "pending",
    approved_by:      null,
    approved_at:      null,
    rejection_reason: null,
    compliance_flags: [],
    compliance_passed: false,
    created_at:       now,
    updated_at:       now,
  };
}

/**
 * Returns a ReviewDraft with updated message text and fresh compliance state.
 * Call this whenever the message_text changes.
 */
export function updateDraftMessage(
  draft:       ReviewDraft,
  messageText: string,
  now:         string,
): ReviewDraft {
  const result = validateDraftCompliance(messageText, now);
  return {
    ...draft,
    message_text:     messageText,
    character_count:  messageText.length,
    compliance_flags: result.flags,
    compliance_passed: result.passed,
    updated_at:       now,
  };
}
