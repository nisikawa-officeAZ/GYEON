// DealerOS — Review Draft Compliance Model
//
// Sprint 11C Phase C: compliance-first review draft model.
//
// This module defines the ReviewDraft type and the compliance framework
// that governs what can and cannot appear in AI-generated review request messages.
//
// CRITICAL COMPLIANCE RULES (permanently enforced — cannot be weakened):
//
//   1. NO FAKE REVIEWS — The system never generates, submits, or simulates
//      reviews on behalf of customers. All reviews must be written by the customer.
//
//   2. NO STAR RATING SUGGESTIONS — Review requests must never suggest,
//      imply, or request a specific number of stars. The message must invite
//      the customer to share their honest opinion.
//
//   3. NO PRESSURE LANGUAGE — The message must be friendly and optional.
//      No urgency ("今すぐ"), no guilt ("お願いします"), no follow-up pressure.
//
//   4. NO INCENTIVES — Review requests must never offer discounts, gifts,
//      points, or any form of reward in exchange for leaving a review.
//      This violates Google Business Profile and most platform review policies.
//
//   5. VOLUNTARY ALWAYS — The message must explicitly state that the customer
//      is free to write whatever they feel. Include: "率直なご感想で問題ございません".
//
//   6. NO AUTO-POSTING — The system must never submit a review on behalf of a
//      customer via any API, automation, or web scraping.
//
//   7. DEALER APPROVAL REQUIRED — No message may be sent without the dealer
//      explicitly pressing a confirm button. Auto-send is permanently disabled.
//
// These rules are implemented as:
//   - REVIEW_COMPLIANCE_RULES constant (machine-readable rule set)
//   - ReviewComplianceFlag enum (specific violation types)
//   - validateDraftCompliance() function (pattern-based static check)
//   - ReviewDraft.compliance_passed field (must be true before dealer approval)

import type { ReviewPlatform }          from "@/lib/ai/agents/reputation/types";
import type { ReviewComplianceFlag }     from "./reputation-types";

// ─── ReviewDraft ──────────────────────────────────────────────────────────────

/**
 * ReviewDraft — a prepared review request or review response message.
 *
 * Represents the message that will eventually be sent to the customer via LINE
 * (draft_type = "review_request") or posted as a response on a review platform
 * (draft_type = "review_response").
 *
 * Compliance invariants enforced at construction:
 *   - compliance_passed must be true before approval_status can be "approved"
 *   - No message with compliance_flags may be approved
 *   - dealer_approved is always false until explicit dealer confirmation
 */
export interface ReviewDraft {
  id:               string;
  /** The ReviewRequest.id this draft is for. */
  request_id:       string;
  /** Always from getCurrentDealer() — never from client input. */
  dealer_id:        string;
  /** Whether this is an outbound review request or an inbound review response. */
  draft_type:       "review_request" | "review_response";
  /** The message text to be sent or posted. Dealer may edit before approval. */
  message_text:     string;
  character_count:  number;
  platform:         ReviewPlatform;
  /** Direct link to the review page included in the message. Null if not yet configured. */
  review_url:       string | null;
  /**
   * Who generated this draft.
   * "ai" = generated by reputation_agent (Phase 11D+).
   * "dealer" = written manually by the dealer.
   */
  prepared_by:      "ai" | "dealer";
  /** AI provider ID if prepared_by = "ai". Null for dealer-written drafts. */
  ai_provider_id:   string | null;
  approval_status:  "pending" | "approved" | "rejected";
  approved_by:      string | null;
  approved_at:      string | null;
  rejection_reason: string | null;
  /**
   * Compliance violations detected by validateDraftCompliance().
   * Must be empty for compliance_passed to be true.
   */
  compliance_flags: ReviewComplianceFlag[];
  /**
   * True when no compliance flags are present.
   * A draft with compliance_passed = false cannot be approved.
   */
  compliance_passed: boolean;
  created_at:       string;   // ISO 8601
  updated_at:       string;   // ISO 8601
}

// ─── Compliance rule model ────────────────────────────────────────────────────

